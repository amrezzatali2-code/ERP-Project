-- =========================================================
-- تصفير قيود العملاء (حذف قيود LedgerEntries المرتبطة بالعملاء)
-- يؤدي إلى ظهور رصيد كل عميل = 0 في تقرير أرصدة العملاء
--
-- تحذير شديد:
-- - سيُحذف كل القيود التي لها CustomerId (فواتير، إيصالات، إشعارات...)
-- - ستفقد تاريخ حركات العملاء في دفتر الأستاذ
-- - لا يمكن استرجاع البيانات بعد التنفيذ
-- - استخدم فقط عند البدء من جديد أو إعادة ضبط كاملة للنظام
--
-- ملاحظة: نفّذ أيضاً ZeroCustomerBalances.sql لتصفير Customer.CurrentBalance
-- =========================================================

BEGIN TRANSACTION;

-- عرض عدد القيود التي سيتم حذفها (للتحقق قبل الحذف)
SELECT COUNT(*) AS [عدد_قيود_العملاء_التي_ستُحذف]
FROM LedgerEntries
WHERE CustomerId IS NOT NULL;

-- حذف قيود العملاء
DELETE FROM LedgerEntries
WHERE CustomerId IS NOT NULL;

SELECT @@ROWCOUNT AS [عدد_القيود_المحذوفة];

-- للتأكيد وتطبيق التغييرات: نفّذ السطر التالي
COMMIT;

-- لإلغاء التغييرات: استخدم ROLLBACK بدلاً من COMMIT
