-- =========================================================
-- تصفير أرصدة العملاء (جعل Customer.CurrentBalance = 0)
-- استخدم هذا السكربت لإعادة ضبط أرصدة العملاء إلى صفر
--
-- ملاحظة:
-- - تقرير أرصدة العملاء يحسب الرصيد من جدول LedgerEntries
--   (مصدر الحقيقة). هذا السكربت يصفّر فقط العمود المخزّن
--   CurrentBalance في جدول Customers (القيمة المخزنة للتسريع).
-- - إذا كان التقرير يظهر أرصدة غير صفرية، فالسبب من قيود
--   LedgerEntries. استخدم زر "إصلاح أرصدة العملاء" في التقرير
--   لمعالجة القيود المتبقية لأذون/إشعارات محذوفة.
-- =========================================================

BEGIN TRANSACTION;

UPDATE Customers
SET CurrentBalance = 0,
    UpdatedAt = GETUTCDATE();

SELECT @@ROWCOUNT AS [عدد_العملاء_المحدثين];

-- للتأكيد وتطبيق التغييرات: نفّذ السطر التالي
COMMIT;

