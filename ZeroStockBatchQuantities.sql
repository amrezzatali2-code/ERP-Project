-- تصفير كميات StockBatch (جعل كل QtyOnHand = 0)
-- استخدم هذا السكربت لإعادة ضبط الأرصدة إلى صفر
--
-- تحذير: سيُفقد كل الرصيد المعروض في تقرير أرصدة الأصناف
-- =========================================================

BEGIN TRANSACTION;

UPDATE StockBatches
SET QtyOnHand = 0,
    UpdatedAt = GETUTCDATE();

SELECT @@ROWCOUNT AS [عدد_الصفوف_المحدثة];

-- للتأكيد وتطبيق التغييرات: نفّذ السطر التالي
COMMIT;

